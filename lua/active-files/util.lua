local M = {}

function M.get_project_root()
    return vim.fn.getcwd()
end

function M.is_project_file(filepath)
    return filepath:find(M.get_project_root(), 1, true) == 1
end

function M.is_readable_file(path)
    return vim.fn.filereadable(path) == 1
end

return M
